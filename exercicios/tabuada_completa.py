for i in range(1,11):
    print(f'Tabuada soma {i} ===============')
    for s in range(1,11): #tabuda soma
        print(f'{i} + {s} = {i+s}')
    print(f'Tabuada subtracao {i} ===============')
    for sub in range(1,11):
        print(f'{i} - {sub} = {i-sub}')
    print(f'Tabuada divisao {i} ===============')
    for d in range(1,11):
        print(f'{i} / {d} = {i/d}')
    print(f'Tabuada multiplicacao {i} ===============')
    for d in range(1,11):
        print(f'{i} x {d} = {i*d}')